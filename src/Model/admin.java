package Model;

/**
 *
 * @author USER
 */
public class admin {
    private String nama_menu;
    private int jumlah;
    private int harga;

    public admin() {
    }
    
    

    public admin(String nama_menu, int jumlah, int harga) {
        this.nama_menu = nama_menu;
        this.jumlah = jumlah;
        this.harga = harga;
    }

    public String getNama_menu() {
        return nama_menu;
    }

    public void setNama_menu(String nama_menu) {
        this.nama_menu = nama_menu;
    }

    public int getJumlah() {
        return jumlah;
    }

    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }
    
    
    
    
    
}
